class Rectangle{
    constructor(width,height){
        this.width=width;
        this.height=height;
    }
}

let obj1 = new Rectangle(3,5);
let obj2 = new Rectangle(7,6);
console.log("height and width of the rectangle1:("+obj1.height+","+obj1.width+")");
console.log("height and width of the rectangle2:("+obj2.height+","+obj2.width+")");

